/**
 * This function checks whether the file extension is a picture format.
 *
 * @param {string} filename File name. For example, 'image.png'.
 */
export const isImage = (filename: string) =>
  extensions.has(
    path.posix
      .extname(filename)
      .slice(1)
      .split("!")[0]
      .split("?")[0]
      .toLowerCase()
  );
